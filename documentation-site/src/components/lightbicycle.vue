              ? $(this).children("b").text()
              : $(this).text() == ""
              ? ""
              : $(this).text();
          $(this).parent().addClass("current");
          if (tds == "") {
            $(this).removeClass("current");
          } else {
            $(this).addClass("current");
          }
        },
