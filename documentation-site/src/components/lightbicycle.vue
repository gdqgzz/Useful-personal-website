                    <p>
                      So why a wider tire is better? Wider road tires reduce the
                      risk of pinch flatting at a given tire pressure and reduce
                      rolling resistance. You can also run lower air pressure
                      which absorbs more road shock, and improve the traction
                      and stability. Wider tires tend to have better grip on web
