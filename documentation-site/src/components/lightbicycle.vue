                    <p class="mt-3"><i>What Are the Flyweight Rims?</i></p>
                    <p>
                      If it isn’t clear from the name, the Flyweights are the
                      lightest rims available from Light Bicycle and, for that
                      matter, some the very lightest available on the market.
                      Despite what you might think, the Flyweight does not refer
                      to a particular rim, but rather a construction process.
                      We’ll talk a lot of exactly what this process is later in
                      this blog, but the short version is that Flyweight rims
                      are built to be as light as possible by using a blend of
                      Toray T700 and T-800 carbon, a new prepreg, and a new
                      layup. The result is that each rim built using the
                      Flyweight construction process drops anywhere from 100-80g
                      compared to the same mould using our standard rim
                      construction. So far Light Bicycle has launched the
                      RM29C06 29” XC rims (280g), the RM6100BC09 27.5” XC rims
                      (260g), and the RG922 700c Gravel rims (380g), ect are
                      using Flyweight construction, but as we continue to
                      develop the process and update our lineup, more and more
                      rims will become available with Flyweight Construction.
                    </p>
                    <p class="mt-5"><i>New Construction Process</i></p>
                    <p>
                      We modified three main processes in our construction to
                      make the Flyweight rims as light as they are: 1) We used a
                      blend of Toray T800 and T-700, 2) We used a lower FAW
                      (fiber areal weight) and new preg and, 3) We used a new
                      layup design at the nipple bed to reduce the amount of
                      material needed.
                    </p>
                    <p>
                      As we were saying, the flyweights rims are built using a
                      mix of Toray T800 and Toray T-700 carbon fiber. What does
                      this mean? Most of the rims that we build and, for that
                      matter, that anyone else builds use exclusively Toray
                      T700. The reason for that is quite simple; T700 is a
                      little heavier, but far more robust than a Toray T800 rim.
                      A rim Toray T800 is would be far too stiff, because of
                      that, be quite brittle and crack easily. However, for the
                      past number of year we have been experimenting with using
                      a blend of Toray T700 and T800 to build a super
                      lightweight rim that is still tough enough to race on. The
                      result of this is the Flyweight construction process.
                    </p>
                    <p>
                      One of the keys to the Flyweight process and what is
                      largely responsible to allowing us to use Toray T700 and
                      T800 in the same rim is our new Prepreg. The new Prepreg
                      has a much lower Fiber Areal Weight and we can create a
                      more adjustable layup. This means that the stress between
                      layers is reduced and the T800 can be used without
                      creating the stiffness problem that typically comes with
                      building a Toray T800 rim.
                    </p>
                    <p>
                      Another feature of the Flyweight construction process is a
                      new nipple bed layup. On the flyweights we build up the
                      layers differently depending on what the spoke hole
                      drilling is going to be. The layup takes quite a bit
                      longer to perform than our standard layup, but results in
                      those no compromise marginal weight gains that we wanted
                      out of a race wheelset.
                    </p>
